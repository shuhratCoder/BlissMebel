const DICTS: Record<Lang, Dict> = { ru, uz }

// ────────────────────────────────────────────────────────────
// STORE (persisted to localStorage)
// ────────────────────────────────────────────────────────────
interface LangStore {
  lang: Lang
  setLang: (lang: Lang) => void
}

export const useLangStore = create<LangStore>()(
  persist(
    (set) => ({
      lang: 'ru',
      setLang: (lang) => set({ lang }),
    }),
    { name: 'furniture-erp-lang', skipHydration: true },
  ),
)

// ────────────────────────────────────────────────────────────
// HELPERS
// ────────────────────────────────────────────────────────────
function interpolate(s: string, vars?: Record<string, string | number>): string {
  if (!vars) return s
  return s.replace(/\{(\w+)\}/g, (_, k) =>
    vars[k] !== undefined ? String(vars[k]) : `{${k}}`,
  )
}

function translate(lang: Lang, key: string, vars?: Record<string, string | number>): string {
  const dict = DICTS[lang] ?? ru
  const raw = dict[key] ?? ru[key] ?? key
  return interpolate(raw, vars)
}

// React hook
export function useT() {
  const lang = useLangStore((s) => s.lang)
  const setLang = useLangStore((s) => s.setLang)
  const t = React.useCallback(
    (key: string, vars?: Record<string, string | number>) =>
      translate(lang, key, vars),
    [lang],
  )
  return { t, lang, setLang }
}

// Non-React (api.ts, hooks/index.ts toasts) helper
export function tStatic(key: string, vars?: Record<string, string | number>): string {
  return translate(useLangStore.getState().lang, key, vars)
}
